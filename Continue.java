public class Continue
{
	public static void main(String[] args) {
	     Scanner sc = new Scanner(System.in);
	    for(int i=1;i<30;i++){        // it will take +ve as well as -ve values or string values .
	    
	       if(i>=12 && i<=28)
	           continue;
	    System.out.println(i);   
	    }
	 
	}
}












